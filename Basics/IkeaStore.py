"""Siddhi goes to Ikea store and purchase 5 chairs at Rs.2000 each and 4 tables at Rs. 10000 each. He got a discount of 25% on the total amount. Write a Python code to calculate and print the final amount"""

chair = 2000
chairs = chair*5
table = 10000
tables = table*4
total = chairs+tables
print(f"So the final amount that Siddhi had to pay is {total*0.25} ")
